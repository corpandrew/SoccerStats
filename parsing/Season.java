package parsing;

import java.util.ArrayList;

public class Season {
    private String identifier;
    private String season_slug;
    private String season_start;
    private String name;
    private String league_identifier;
    private String season_end;

    public String getIdentifier() {
        return identifier;
    }

    public String getSeason_slug() {
        return season_slug;
    }

    public String getSeason_start() {
        return season_start;
    }

    public String getName() {
        return name;
    }

    public String getLeague_identifier() {
        return league_identifier;
    }

    public String getSeason_end() {
        return season_end;
    }
}
